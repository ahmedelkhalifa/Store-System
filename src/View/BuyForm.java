    private void amountInputStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_amountInputStateChanged
        int amount = (int)amountInput.getValue();
        if(amount > 0)
        {
            double totalPrice = Double.valueOf(priceInput.getText()) * (Integer)amountInput.getValue();
            totalPriceInput.setText("" + totalPrice);               
        }
        else
        {
            amountInput.setValue(Integer.valueOf("0"));
            totalPriceInput.setText("");
        }
    }//GEN-LAST:event_amountInputStateChanged

