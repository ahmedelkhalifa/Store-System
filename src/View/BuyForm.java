    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        Connection con = DB.getConnection();
        String sql = "select sum(total_price) from temporary_products";
        double totalPrice = 0;
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next())
            {
                totalPrice = rs.getDouble("sum(total_price)");
            }
        } catch (SQLException ex) {
            Logger.getLogger(BuyForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(totalPrice != 0)
        {
            int response = JOptionPane.showConfirmDialog(null, "Confirm buying this? with total price = " + totalPrice, "Confirm", JOptionPane.YES_NO_OPTION);
            if(response == 0)
            {
                ProductDaoImp dao = new ProductDaoImp();
                dao.buy(totalPrice);
                try {
                    PreparedStatement del = con.prepareStatement("delete from temporary_products");
                    del.execute();
                    del.close();
                    load();
                } catch (SQLException ex) {
                    Logger.getLogger(BuyForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else
            JOptionPane.showMessageDialog(null, "no products found");
    }//GEN-LAST:event_confirmButtonActionPerformed

