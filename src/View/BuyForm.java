    public void load()
    {
        ProductDaoImp dao = new ProductDaoImp();
        List<Product> list = dao.list();
        DefaultTableModel dft = (DefaultTableModel) productsTable.getModel();
        dft.setRowCount(0);
        for(Product prod : list)
        {
            dft.addRow(new Object[]{prod.getName(),prod.getPrice(),prod.getSupplier(),prod.getAmount(),prod.getTotalPrice()});    
        }
        productsTable.setModel(dft);
    }

