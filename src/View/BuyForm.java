    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Product prod = new Product();
        prod.setName(productsBox.getSelectedItem().toString());
        prod.setSupplier(supplierInput.getText());
        prod.setPrice(Double.parseDouble(priceInput.getText()));
        prod.setAmount((Integer)amountInput.getValue());
        prod.setTotalPrice(prod.getAmount() * prod.getPrice());
        ProductDaoImp dao = new ProductDaoImp();
        dao.setProductsTable(prod);
        amountInput.setValue(Integer.valueOf("0"));
        productsBox.setSelectedIndex(-1);
        supplierInput.setText("");
        priceInput.setText("");
        productsBox.requestFocus();
        load();
    }//GEN-LAST:event_addButtonActionPerformed

