    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        int response = -1;
        LocalDate date = LocalDate.now();
        if(customerButton.isSelected())
        {
            try
            {
                response = 0;  
                Order ord = new Order();
                ord.setName(nameInput.getText());
                ord.setCustomerName(customersBox.getSelectedItem().toString());
                ord.setCustomerId(Integer.parseInt(custIdInput.getText()));
                ord.setAddress(addressInput.getText());
                Calendar c = new GregorianCalendar();
                ord.setOrderDate(date);
                ProductDaoImp dao = new ProductDaoImp();
                dao.sell(response, ord,customersBox.getSelectedItem().toString());
                Customer cr = dao.getCustomer(customersBox.getSelectedItem().toString());
                dao.showBill(cr);
                Connection con = DB.getConnection();
                String sql = "delete from sell_products";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.execute();
                ps.close();
                load();
                nameInput.setText("");
                customersBox.setSelectedIndex(-1);
                custIdInput.setText("");
                addressInput.setText("");
            } catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Some fields are empty", "Oops", JOptionPane.WARNING_MESSAGE);
            }
            
        }
        else if(guest.isSelected())
        {
            response = 1;
            Order ord = new Order();
            ord.setName(nameInput.getText());
            Calendar c = new GregorianCalendar();
            ord.setOrderDate(date);
            ProductDaoImp dao = new ProductDaoImp();
            dao.sell(response, ord,"");
            Connection con = DB.getConnection();
            dao.showBill();
            String sql = "delete from sell_products";
            PreparedStatement ps;
            try {
                ps = con.prepareStatement(sql);
                ps.execute();
                ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(SellForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            load();
            nameInput.setText("");
            customersBox.setSelectedIndex(-1);
            custIdInput.setText("");
            addressInput.setText("");  
        }
        
        
    }//GEN-LAST:event_confirmButtonActionPerformed

