    public void load()
    {
        Connection con = DB.getConnection();
        String sql = "select * from sell_products";
        List<Product> list = new ArrayList();
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while(rs.next())
            {
                Product prod = new Product();
                prod.setName(rs.getString("product"));
                prod.setPrice(rs.getDouble("price"));
                prod.setAmount(rs.getInt("amount"));
                prod.setTotalPrice(rs.getDouble("total_price"));
                list.add(prod);
            }
            DefaultTableModel dft = (DefaultTableModel) productsTable.getModel();
            dft.setRowCount(0);
            for(Product prod : list)
            {
                dft.addRow(new Object[]{prod.getName(),prod.getPrice(),prod.getAmount(),prod.getTotalPrice()});
            }
            productsTable.setModel(dft);
        } catch (SQLException ex) {
            Logger.getLogger(SellForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

