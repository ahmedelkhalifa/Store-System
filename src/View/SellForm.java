    private void amountInputStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_amountInputStateChanged
        int sellAmount = (int)amountInput.getValue();
        if(sellAmount >= 0)
        {  
            int totalAmount = ProductDaoImp.checkQuantity(productsBox.getSelectedItem().toString());
            String amount = "";
            if(sellAmount <= totalAmount)
                totalPrice.setText(((int)amountInput.getValue()*Double.parseDouble(priceInput.getText())+""));
            else
                amountInput.setValue(Integer.valueOf(totalAmount + ""));
        }
        else
            amountInput.setValue(Integer.valueOf("0"));
    }//GEN-LAST:event_amountInputStateChanged

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Product prod = new Product();
        prod.setName(productsBox.getSelectedItem().toString());
        prod.setPrice(Double.parseDouble(priceInput.getText()));
        prod.setAmount((Integer)amountInput.getValue());
        prod.setTotalPrice(Double.parseDouble(totalPrice.getText()));
        ProductDaoImp dao = new ProductDaoImp();
        dao.addSellProduct(prod);
        amountInput.setValue(Integer.valueOf("0"));
        productsBox.setSelectedIndex(-1);
        priceInput.setText("");
        productsBox.requestFocus();
        load();
    }//GEN-LAST:event_addButtonActionPerformed

